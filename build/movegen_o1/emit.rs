//! Emit `generated_tables.rs` for the engine to `include!`.

use std::fs::File;
use std::io::{BufWriter, Write};
use std::path::Path;
use std::time::Instant;

use super::pawn::{
    discover_all_pawn_tables, ENEMY_LAYERS, MAX_WALL_SLOTS, PHYS_WALL_COMBOS, WALL_KEYS,
};
use super::progress::phase_bar;
use super::wall::{
    discover_all_wall_tables, MAX_PSEUDO_PROBES, MAX_TOPO_PROBES, PHYS_PSEUDO_COMBOS,
    PHYS_TOPO_COMBOS, WALL_PSEUDO_KEYS, WALL_TOPO_KEYS,
};

pub fn generate(out_path: &Path) {
    let t0 = Instant::now();
    if let Some(parent) = out_path.parent() {
        std::fs::create_dir_all(parent).expect("create output dir");
    }

    let bar = phase_bar();
    let pawn = discover_all_pawn_tables(&bar);
    let (wall, wall_pseudo, wall_topo) = discover_all_wall_tables(&bar);

    let file = File::create(out_path).expect("create tables file");
    let mut w = BufWriter::new(file);

    writeln!(w, "// @generated by movegen-o1-gen — do not edit").unwrap();
    writeln!(w, "// Regenerate: cargo run --bin movegen-o1-gen").unwrap();
    writeln!(
        w,
        "// PAWN_LEGAL[sq][enemy_key][wall_key] -> 12-bit pseudo-move mask"
    )
    .unwrap();
    writeln!(
        w,
        "// wall_key = semantic (<=256); physical combo remapped via generated_remap.bin"
    )
    .unwrap();
    writeln!(w).unwrap();

    writeln!(w, "pub const PHYS_WALL_COMBOS: usize = {PHYS_WALL_COMBOS};").unwrap();
    writeln!(w).unwrap();

    let remap_path = out_path
        .parent()
        .expect("parent dir")
        .join("generated_remap.bin");
    let mut remap_bytes = Vec::with_capacity(81 * ENEMY_LAYERS * PHYS_WALL_COMBOS);
    for p in &pawn {
        for layer in &p.layers {
            remap_bytes.extend_from_slice(&layer.wall_remap);
            if layer.wall_remap.len() < PHYS_WALL_COMBOS {
                remap_bytes.resize(
                    remap_bytes.len() + (PHYS_WALL_COMBOS - layer.wall_remap.len()),
                    0,
                );
            }
        }
    }
    std::fs::write(&remap_path, &remap_bytes).expect("write remap bin");
    eprintln!(
        "movegen-o1-gen: remap {} bytes → {}",
        remap_bytes.len(),
        remap_path.display()
    );

    writeln!(w, "pub const PAWN_CATALOG: [[u8; 12]; 81] = [").unwrap();
    for p in &pawn {
        writeln!(w, "    {:?},", p.catalog).unwrap();
    }
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();

    writeln!(w, "pub const PAWN_LAYER_VALID: [[u8; 5]; 81] = [").unwrap();
    for p in &pawn {
        let row: Vec<u8> = p.layers.iter().map(|l| u8::from(l.valid)).collect();
        writeln!(w, "    {:?},", row).unwrap();
    }
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();

    writeln!(w, "pub const PAWN_WALL_COMBO_COUNT: [[u16; 5]; 81] = [").unwrap();
    for p in &pawn {
        let row: Vec<u16> = p.layers.iter().map(|l| l.wall_combo_count).collect();
        writeln!(w, "    {:?},", row).unwrap();
    }
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();

    writeln!(w, "pub const PAWN_WALL_SLOT_COUNT: [[u8; 5]; 81] = [").unwrap();
    for p in &pawn {
        let row: Vec<u8> = p.layers.iter().map(|l| l.wall_bits.len() as u8).collect();
        writeln!(w, "    {:?},", row).unwrap();
    }
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();

    write_wall_desc(&mut w, "PAWN_WALL_DESC_ROW", &pawn, |l, i| {
        l.wall_bits.get(i).map(|&(r, _, _)| r).unwrap_or(255)
    });
    write_wall_desc(&mut w, "PAWN_WALL_DESC_COL", &pawn, |l, i| {
        l.wall_bits.get(i).map(|&(_, c, _)| c).unwrap_or(255)
    });
    write_wall_desc(&mut w, "PAWN_WALL_DESC_H", &pawn, |l, i| {
        l.wall_bits
            .get(i)
            .map(|&(_, _, h)| u8::from(h))
            .unwrap_or(255)
    });

    writeln!(
        w,
        "pub const PAWN_LEGAL: [[[u16; {WALL_KEYS}]; {ENEMY_LAYERS}]; 81] = ["
    )
    .unwrap();
    bar.begin(81, "emit tables.rs");
    for (sq, p) in pawn.iter().enumerate() {
        writeln!(w, "    [").unwrap();
        for layer in &p.layers {
            write!(w, "        [").unwrap();
            for (i, &mv) in layer.table.iter().enumerate() {
                if i > 0 {
                    write!(w, ",").unwrap();
                }
                if i % 32 == 0 {
                    write!(w, "\n            ").unwrap();
                }
                write!(w, "{mv}").unwrap();
            }
            writeln!(w, "\n        ],").unwrap();
        }
        writeln!(w, "    ],").unwrap();
        bar.tick(&format!("write square {sq}/80"));
    }
    bar.finish("tables written");
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();

    writeln!(w, "pub const WALL_COLLISION_MASK: [u8; 128] = [").unwrap();
    for slot in &wall {
        writeln!(w, "    {},", slot.collision_mask).unwrap();
    }
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();

    writeln!(w, "pub const WALL_PHYSICAL_TABLE: [[u8; 256]; 128] = [").unwrap();
    for slot in &wall {
        write!(w, "    [").unwrap();
        for (i, &ok) in slot.table.iter().enumerate() {
            if i > 0 {
                write!(w, ",").unwrap();
            }
            write!(w, "{}", u8::from(ok)).unwrap();
        }
        writeln!(w, "],").unwrap();
    }
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();

    writeln!(w, "pub const WALL_SLOT_ROW: [u8; 128] = [").unwrap();
    for slot in &wall {
        writeln!(w, "    {},", slot.row).unwrap();
    }
    writeln!(w, "];").unwrap();

    writeln!(w, "pub const WALL_SLOT_COL: [u8; 128] = [").unwrap();
    for slot in &wall {
        writeln!(w, "    {},", slot.col).unwrap();
    }
    writeln!(w, "];").unwrap();

    writeln!(w, "pub const WALL_SLOT_HORIZONTAL: [u8; 128] = [").unwrap();
    for slot in &wall {
        writeln!(w, "    {},", u8::from(slot.horizontal)).unwrap();
    }
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();

    let pseudo_h_remap_path = out_path
        .parent()
        .expect("parent dir")
        .join("generated_wall_pseudo_h_remap.bin");
    let pseudo_v_remap_path = out_path
        .parent()
        .expect("parent dir")
        .join("generated_wall_pseudo_v_remap.bin");
    write_padded_remap(
        &pseudo_h_remap_path,
        &wall_pseudo.h_remap,
        PHYS_PSEUDO_COMBOS,
    );
    write_padded_remap(
        &pseudo_v_remap_path,
        &wall_pseudo.v_remap,
        PHYS_PSEUDO_COMBOS,
    );

    writeln!(
        w,
        "// Layer-1 pseudo-legal wall masks (overlap/cross only — no pawns, no path flood)."
    )
    .unwrap();
    writeln!(
        w,
        "pub const PHYS_PSEUDO_COMBOS: usize = {PHYS_PSEUDO_COMBOS};"
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_H_PROBE_COUNT: u8 = {};",
        wall_pseudo.h_probes.len()
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_V_PROBE_COUNT: u8 = {};",
        wall_pseudo.v_probes.len()
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_H_KEY_COUNT: u16 = {};",
        wall_pseudo.h_key_count
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_V_KEY_COUNT: u16 = {};",
        wall_pseudo.v_key_count
    )
    .unwrap();
    writeln!(w).unwrap();

    writeln!(
        w,
        "pub const WALL_PSEUDO_H_PROBE_ROW: [u8; {MAX_PSEUDO_PROBES}] = {:?};",
        probe_pad(&wall_pseudo.h_probes, |p| p.0)
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_H_PROBE_COL: [u8; {MAX_PSEUDO_PROBES}] = {:?};",
        probe_pad(&wall_pseudo.h_probes, |p| p.1)
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_H_PROBE_H: [u8; {MAX_PSEUDO_PROBES}] = {:?};",
        probe_pad(&wall_pseudo.h_probes, |p| u8::from(p.2))
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_V_PROBE_ROW: [u8; {MAX_PSEUDO_PROBES}] = {:?};",
        probe_pad(&wall_pseudo.v_probes, |p| p.0)
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_V_PROBE_COL: [u8; {MAX_PSEUDO_PROBES}] = {:?};",
        probe_pad(&wall_pseudo.v_probes, |p| p.1)
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_PSEUDO_V_PROBE_H: [u8; {MAX_PSEUDO_PROBES}] = {:?};",
        probe_pad(&wall_pseudo.v_probes, |p| u8::from(p.2))
    )
    .unwrap();
    writeln!(w).unwrap();

    write_u64_table(
        &mut w,
        "WALL_PSEUDO_H",
        &wall_pseudo.h_table,
        WALL_PSEUDO_KEYS,
    );
    write_u64_table(
        &mut w,
        "WALL_PSEUDO_V",
        &wall_pseudo.v_table,
        WALL_PSEUDO_KEYS,
    );

    let topo_h_remap_path = out_path
        .parent()
        .expect("parent dir")
        .join("generated_wall_topo_h_remap.bin");
    let topo_v_remap_path = out_path
        .parent()
        .expect("parent dir")
        .join("generated_wall_topo_v_remap.bin");
    write_padded_remap(&topo_h_remap_path, &wall_topo.h_remap, PHYS_TOPO_COMBOS);
    write_padded_remap(&topo_v_remap_path, &wall_topo.v_remap, PHYS_TOPO_COMBOS);

    writeln!(
        w,
        "// Topology flood-skip (`can_wall_block_topology`) — O(1) opt between L2 and L3."
    )
    .unwrap();
    writeln!(w, "pub const PHYS_TOPO_COMBOS: usize = {PHYS_TOPO_COMBOS};").unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_H_PROBE_COUNT: u8 = {};",
        wall_topo.h_probes.len()
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_V_PROBE_COUNT: u8 = {};",
        wall_topo.v_probes.len()
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_H_KEY_COUNT: u16 = {};",
        wall_topo.h_key_count
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_V_KEY_COUNT: u16 = {};",
        wall_topo.v_key_count
    )
    .unwrap();
    writeln!(w).unwrap();

    writeln!(
        w,
        "pub const WALL_TOPO_H_PROBE_ROW: [u8; {MAX_TOPO_PROBES}] = {:?};",
        topo_probe_pad(&wall_topo.h_probes, |p| p.0)
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_H_PROBE_COL: [u8; {MAX_TOPO_PROBES}] = {:?};",
        topo_probe_pad(&wall_topo.h_probes, |p| p.1)
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_H_PROBE_H: [u8; {MAX_TOPO_PROBES}] = {:?};",
        topo_probe_pad(&wall_topo.h_probes, |p| u8::from(p.2))
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_V_PROBE_ROW: [u8; {MAX_TOPO_PROBES}] = {:?};",
        topo_probe_pad(&wall_topo.v_probes, |p| p.0)
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_V_PROBE_COL: [u8; {MAX_TOPO_PROBES}] = {:?};",
        topo_probe_pad(&wall_topo.v_probes, |p| p.1)
    )
    .unwrap();
    writeln!(
        w,
        "pub const WALL_TOPO_V_PROBE_H: [u8; {MAX_TOPO_PROBES}] = {:?};",
        topo_probe_pad(&wall_topo.v_probes, |p| u8::from(p.2))
    )
    .unwrap();
    writeln!(w).unwrap();

    write_u64_table(&mut w, "WALL_TOPO_H", &wall_topo.h_table, WALL_TOPO_KEYS);
    write_u64_table(&mut w, "WALL_TOPO_V", &wall_topo.v_table, WALL_TOPO_KEYS);

    w.flush().unwrap();
    eprintln!(
        "movegen-o1-gen: finished in {:.1}s → {}",
        t0.elapsed().as_secs_f64(),
        out_path.display()
    );
}

fn write_padded_remap(path: &std::path::Path, remap: &[u8], pad_to: usize) {
    let mut bytes = remap.to_vec();
    if bytes.len() < pad_to {
        bytes.resize(pad_to, 0);
    }
    std::fs::write(path, &bytes).expect("write remap bin");
    eprintln!(
        "movegen-o1-gen: remap {} bytes → {}",
        bytes.len(),
        path.display()
    );
}

fn write_u64_table(w: &mut BufWriter<File>, name: &str, table: &[u64; 1024], len: usize) {
    writeln!(w, "pub const {name}: [u64; {len}] = [").unwrap();
    for (i, &mv) in table.iter().enumerate() {
        if i > 0 {
            write!(w, ",").unwrap();
        }
        if i % 8 == 0 {
            write!(w, "\n    ").unwrap();
        }
        write!(w, "{mv}").unwrap();
    }
    writeln!(w, "\n];").unwrap();
}

fn topo_probe_pad<F>(probes: &[(u8, u8, bool)], f: F) -> Vec<u8>
where
    F: Fn(&(u8, u8, bool)) -> u8,
{
    let mut row: Vec<u8> = probes.iter().map(f).collect();
    row.resize(MAX_TOPO_PROBES, 255);
    row
}

fn probe_pad<F>(probes: &[(u8, u8, bool)], f: F) -> Vec<u8>
where
    F: Fn(&(u8, u8, bool)) -> u8,
{
    let mut row: Vec<u8> = probes.iter().map(f).collect();
    row.resize(MAX_PSEUDO_PROBES, 255);
    row
}

fn write_wall_desc<F>(
    w: &mut BufWriter<File>,
    name: &str,
    pawn: &[super::pawn::PawnSquareMeta],
    f: F,
) where
    F: Fn(&super::pawn::EnemyLayerMeta, usize) -> u8,
{
    writeln!(w, "pub const {name}: [[[u8; {MAX_WALL_SLOTS}]; 5]; 81] = [").unwrap();
    for p in pawn {
        writeln!(w, "    [").unwrap();
        for layer in &p.layers {
            let row: Vec<u8> = (0..MAX_WALL_SLOTS).map(|i| f(layer, i)).collect();
            writeln!(w, "        {:?},", row).unwrap();
        }
        writeln!(w, "    ],").unwrap();
    }
    writeln!(w, "];").unwrap();
    writeln!(w).unwrap();
}
